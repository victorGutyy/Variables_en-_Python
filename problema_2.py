# Problema 2: Subcadenas y métodos
# Crear una cadena larga
cadena_larga = "Aprender Python es divertido y útil."

# Dividir la cadena en una lista de palabras
palabras = cadena_larga.split()

# Buscar la posición de una subcadena
posicion = cadena_larga.find("Python")

print("Lista de palabras:", palabras)
print("Posición de 'Python':", posicion)
