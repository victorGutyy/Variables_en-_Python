# Problema 5: Operaciones aritméticas
# Crear dos variables numéricas
a = 10
b = 5

# Realizar operaciones aritméticas
suma = a + b
resta = a - b
multiplicacion = a * b
division = a / b
exponente = a ** b

print("Suma:", suma)
print("Resta:", resta)
print("Multiplicación:", multiplicacion)
print("División:", division)
print("Exponente:", exponente)
